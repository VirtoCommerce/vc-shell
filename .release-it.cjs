const isPrerelease =
  !!process.env.PRERELEASE_CHANNEL && process.env.PRERELEASE_CHANNEL !== "none";
const { execSync } = require("node:child_process");

const COMMIT_TYPE_TITLES = {
  feat: "Features",
  fix: "Bug Fixes",
  perf: "Performance Improvements",
  revert: "Reverts",
  docs: "Documentation",
  style: "Styles",
  refactor: "Code Refactoring",
  test: "Tests",
  build: "Build System",
  ci: "Continuous Integration",
  chore: "Chores",
};

const COMMIT_GROUP_ORDER = {
  "BREAKING CHANGES": 0,
  Features: 1,
  "Bug Fixes": 2,
  "Performance Improvements": 3,
  Reverts: 4,
  "Code Refactoring": 5,
  Documentation: 6,
  "Build System": 7,
  "Continuous Integration": 8,
  Tests: 9,
  Styles: 10,
  Chores: 11,
  "Other Changes": 12,
};

function escapeGitHubMentions(text) {
  return text.replace(/@([\w-]+(?:\/[\w-]+)*)/g, "`@$1`");
}

function createStableWriterOpts() {
  return {
    // Keep all commits in one stable section instead of splitting on alpha/beta/rc tags.
    generateOn: false,
    transform: (commit) => {
      const notes = Array.isArray(commit.notes)
        ? commit.notes.map((note) => ({ ...note, title: "BREAKING CHANGES" }))
        : [];

      const typeKey = typeof commit.type === "string" ? commit.type.toLowerCase() : "";
      const type = COMMIT_TYPE_TITLES[typeKey] || typeKey || "Other Changes";
      const subject =
        typeof commit.subject === "string" && commit.subject.trim()
          ? commit.subject
          : typeof commit.header === "string"
            ? commit.header
            : "";

      if (!subject && notes.length === 0) {
        return undefined;
      }

      return {
        ...commit,
        notes,
        type,
        scope: commit.scope === "*" ? "" : commit.scope,
        shortHash: typeof commit.hash === "string" ? commit.hash.slice(0, 7) : commit.shortHash,
        subject: escapeGitHubMentions(subject),
        references: Array.isArray(commit.references) ? commit.references : [],
      };
    },
    groupBy: "type",
    commitGroupsSort: (a, b) => {
      const ao = COMMIT_GROUP_ORDER[a.title] ?? 99;
      const bo = COMMIT_GROUP_ORDER[b.title] ?? 99;
      return ao === bo ? String(a.title).localeCompare(String(b.title)) : ao - bo;
    },
    commitsSort: ["scope", "subject"],
  };
}

function getLastStableTag() {
  try {
    return execSync("git tag --sort=-v:refname", { encoding: "utf8" })
      .split("\n")
      .map((tag) => tag.trim())
      .find((tag) => /^v\d+\.\d+\.\d+$/.test(tag));
  } catch {
    return undefined;
  }
}

const lastStableTag = isPrerelease ? undefined : getLastStableTag();

/** @type {import("release-it").Config} */
module.exports = {
  git: {
    requireCleanWorkingDir: true,
    requireBranch: ["main"],
    requireUpstream: true,
    requireCommits: isPrerelease,
    tagName: "v${version}",
    commitMessage: "release: v${version}",
    tagAnnotation: "v${version}",
  },
  npm: {
    publish: false,
  },
  github: {
    // Create a GitHub Release entry only for stable releases; prereleases
    // (alpha/beta/rc) only produce git tags + npm publish to avoid cluttering
    // the Releases tab with intermediate dev versions.
    release: !isPrerelease,
    releaseName: "v${version}",
    draft: false,
  },
  plugins: {
    "@release-it/conventional-changelog": {
      preset: { name: "angular" },
      // CHANGELOG files are generated by scripts/release-hooks.ts.
      infile: false,
      ...(isPrerelease
        ? {}
        : {
            ...(lastStableTag
              ? {
                  // Force stable baseline for release-it changelog preview/notes.
                  context: { previousTag: lastStableTag },
                  gitRawCommitsOpts: { from: lastStableTag, merges: null },
                }
              : {}),
            writerOpts: createStableWriterOpts(),
          }),
    },
  },
  hooks: {
    "before:release": "tsx scripts/release-hooks.ts before-release ${version}",
    "after:bump":
      "tsx scripts/release-hooks.ts after-bump ${version} && YARN_ENABLE_IMMUTABLE_INSTALLS=false yarn install",
    "before:stage": isPrerelease
      ? "git add cli/ configs/ packages/ framework/package.json framework/CHANGELOG.md apps/ yarn.lock"
      : "git add cli/ configs/ packages/ framework/package.json apps/ CHANGELOG.md yarn.lock",
  },
};
